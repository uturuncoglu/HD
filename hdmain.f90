!-----------------------------------------------------------------------
!     HD main program 
!-----------------------------------------------------------------------
      program hdmain
!
!-----------------------------------------------------------------------
!     Used module declarations 
!-----------------------------------------------------------------------
!
      use mod_hd_iface
      use mod_hd_param, only : pstep, nstep
!
      implicit none
!
!-----------------------------------------------------------------------
!     Local variable declarations  
!-----------------------------------------------------------------------
!
      integer :: ios, istep
!
!-----------------------------------------------------------------------
!     Initialize  
!-----------------------------------------------------------------------
!
      call hd_init()
!
!-----------------------------------------------------------------------
!     Run 
!-----------------------------------------------------------------------
!
      if (pstep == 0) pstep = pstep+1
      call hd_run(pstep, nstep)
!
!-----------------------------------------------------------------------
!     Finalize
!-----------------------------------------------------------------------
!
      call hd_finalize()
!
      end program hdmain
